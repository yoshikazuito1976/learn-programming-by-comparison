#!/bin/bash

echo "=== command result variable ==="

echo
echo "--------------------------------"
echo "1. 文字列として変数に入れる"
echo "記法: c=\"ls -l\""
echo "--------------------------------"

c="ls -l"

echo "実行結果:"
echo "$c"

echo
echo "--------------------------------"
echo "2. ダブルクォートで変数を表示する"
echo "記法: echo \"\$c\""
echo "--------------------------------"

echo "実行結果:"
echo "$c"

echo
echo "--------------------------------"
echo "3. シングルクォートで変数を表示する"
echo "記法: echo '\$c'"
echo "--------------------------------"

echo "実行結果:"
echo '$c'

echo
echo "--------------------------------"
echo "4. コマンドの実行結果を変数に入れる"
echo "記法: c=\$(ls -l)"
echo "--------------------------------"

c=$(ls -l)

echo "実行結果:"
echo "$c"

echo
echo "--------------------------------"
echo "5. 昔の書き方：バッククォート"
echo "記法: c=\`ls -l\`"
echo "--------------------------------"

c=`ls -l`

echo "実行結果:"
echo "$c"

echo
echo "--------------------------------"
echo "まとめ"
echo "--------------------------------"
echo "\"ls -l\" は、文字列として保存する。"
echo "\$(ls -l) は、コマンドを実行して、その結果を保存する。"
echo "\`ls -l\` も実行結果を保存する古い書き方。"
echo "自分で書くときは \$(...) を使うのがおすすめ。"
echo "ただし、古いコードを読むためにバッククォートも知っておく。"

